#include "libft.h"

int	ft_isalpha(int alpha)
{
	if (alpha >= 65 && alpha <= 90)
	{
		return (1);
	}
	else if (alpha >= 97 && alpha <= 122)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
